PRINT MACRO P1 
CALL NEWLINE
MOV DX,OFFSET P1
MOV AH,9
INT 21H   
ENDM

.MODEL SMALL
.DATA  
USER1 DW 'ENTER YOUR STRING = $'   
USER2 DW 'STRING IS = $'
USER3 DW 'TOTAL STRING COUNT = $'
ARR DB 20 DUP('$')
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    MOV BX,0
    MOV SI,OFFSET ARR
    PRINT USER1
    L1:
    MOV AH,1
    INT 21H
    CMP AL,13
    JE PROGRAMEND
    MOV [SI],AL
    INCREMENT BX
    INC SI
    JMP L1
    
    PROGRAMEND:
    PRINT USER2
    MOV DX,OFFSET ARR
    MOV AH,9
    INT 21H
    
    PRINT USER3
    ADD BX,48
    MOV DX,BX
    MOV AH,02H
    INT 21H  
    
    MOV AH,4CH
    INT 21H  
    MAIN ENDP

INCREMENT MACRO I
    INC I
    ENDM

NEWLINE PROC
    MOV DX,10
    MOV AH,2
    INT 21H
    
    MOV DX,13
    MOV AH,2
    INT 21H 
     RET
NEWLINE ENDP
END
